package booker

import (
	"github.com/iotaledger/hive.go/core/generics/event"

	"github.com/iotaledger/goshimmer/packages/protocol/engine/tangle/booker/markermanager"
	"github.com/iotaledger/goshimmer/packages/protocol/engine/tangle/booker/markers"
	"github.com/iotaledger/goshimmer/packages/protocol/ledger/utxo"
)

type Events struct {
	BlockBooked         *event.Linkable[*Block, Events, *Events]
	BlockConflictAdded  *event.Linkable[*BlockConflictAddedEvent, Events, *Events]
	MarkerConflictAdded *event.Linkable[*MarkerConflictAddedEvent, Events, *Events]
	SequenceEvicted     *event.Linkable[markers.SequenceID, Events, *Events]
	Error               *event.Linkable[error, Events, *Events]

	MarkerManager *markermanager.Events

	event.LinkableCollection[Events, *Events]
}

// NewEvents contains the constructor of the Events object (it is generated by a generic factory).
var NewEvents = event.LinkableCollectionConstructor[Events](func(e *Events) {
	e.BlockBooked = event.Link(event.NewLinkable[*Block, Events](), e, func(target *Events) { e.BlockBooked.LinkTo(target.BlockBooked) })
	e.BlockConflictAdded = event.Link(event.NewLinkable[*BlockConflictAddedEvent, Events](), e, func(target *Events) { e.BlockConflictAdded.LinkTo(target.BlockConflictAdded) })
	e.MarkerConflictAdded = event.Link(event.NewLinkable[*MarkerConflictAddedEvent, Events](), e, func(target *Events) { e.MarkerConflictAdded.LinkTo(target.MarkerConflictAdded) })
	e.SequenceEvicted = event.Link(event.NewLinkable[markers.SequenceID, Events](), e, func(target *Events) { e.SequenceEvicted.LinkTo(target.SequenceEvicted) })
	e.Error = event.Link(event.NewLinkable[error, Events](), e, func(target *Events) { e.Error.LinkTo(target.Error) })

	e.MarkerManager = event.Link(markermanager.NewEvents(), e, func(target *Events) { e.MarkerManager.LinkTo(target.MarkerManager) })
})

type BlockConflictAddedEvent struct {
	Block             *Block
	ConflictID        utxo.TransactionID
	ParentConflictIDs utxo.TransactionIDs
}

type MarkerConflictAddedEvent struct {
	Marker            markers.Marker
	ConflictID        utxo.TransactionID
	ParentConflictIDs utxo.TransactionIDs
}
