package engine

import (
	"github.com/iotaledger/hive.go/core/generics/event"

	"github.com/iotaledger/goshimmer/packages/protocol/chain/engine/clock"
	"github.com/iotaledger/goshimmer/packages/protocol/chain/engine/congestioncontrol"
	"github.com/iotaledger/goshimmer/packages/protocol/chain/engine/consensus"
	"github.com/iotaledger/goshimmer/packages/protocol/chain/engine/tangle"
	"github.com/iotaledger/goshimmer/packages/protocol/chain/engine/tip"
)

type Events struct {
	Clock             *clock.Events
	Tangle            *tangle.Events
	Consensus         *consensus.Events
	CongestionControl *congestioncontrol.Events
	TipManager        *tip.Events

	event.LinkableCollection[Events, *Events]
}

// NewEvents contains the constructor of the Events object (it is generated by a generic factory).
var NewEvents = event.LinkableConstructor(func() (newEvents *Events) {
	return &Events{
		Clock:             clock.NewEvents(),
		Tangle:            tangle.NewEvents(),
		Consensus:         consensus.NewEvents(),
		CongestionControl: congestioncontrol.NewEvents(),
		TipManager:        tip.NewEvents(),
	}
})
