package network

import (
	"github.com/iotaledger/hive.go/core/generics/event"
	"github.com/iotaledger/hive.go/core/identity"

	"github.com/iotaledger/goshimmer/packages/core/commitment"
	"github.com/iotaledger/goshimmer/packages/protocol/models"
)

// region Events ///////////////////////////////////////////////////////////////////////////////////////////////////////

type Events struct {
	BlockReceived                  *event.Linkable[*BlockReceivedEvent, Events, *Events]
	BlockRequestReceived           *event.Linkable[*BlockRequestReceivedEvent, Events, *Events]
	EpochCommitmentReceived        *event.Linkable[*EpochCommitmentReceivedEvent, Events, *Events]
	EpochCommitmentRequestReceived *event.Linkable[*EpochCommitmentRequestReceivedEvent, Events, *Events]
	Error                          *event.Linkable[*ErrorEvent, Events, *Events]

	event.LinkableCollection[Events, *Events]
}

// NewEvents contains the constructor of the Events object (it is generated by a generic factory).
var NewEvents = event.LinkableConstructor(func() (newEvents *Events) {
	return &Events{
		BlockReceived:                  event.NewLinkable[*BlockReceivedEvent, Events](),
		BlockRequestReceived:           event.NewLinkable[*BlockRequestReceivedEvent, Events](),
		EpochCommitmentReceived:        event.NewLinkable[*EpochCommitmentReceivedEvent, Events, *Events](),
		EpochCommitmentRequestReceived: event.NewLinkable[*EpochCommitmentRequestReceivedEvent, Events, *Events](),
		Error:                          event.NewLinkable[*ErrorEvent, Events, *Events](),
	}
})

// endregion ///////////////////////////////////////////////////////////////////////////////////////////////////////////

// region BlockReceivedEvent ///////////////////////////////////////////////////////////////////////////////////////////

type BlockReceivedEvent struct {
	Block *models.Block

	Source identity.ID
}

// endregion ///////////////////////////////////////////////////////////////////////////////////////////////////////////

// region BlockRequestReceivedEvent ////////////////////////////////////////////////////////////////////////////////////

type BlockRequestReceivedEvent struct {
	BlockID models.BlockID

	Source identity.ID
}

// endregion ///////////////////////////////////////////////////////////////////////////////////////////////////////////

// region EpochCommitmentReceivedEvent /////////////////////////////////////////////////////////////////////////////////

type EpochCommitmentReceivedEvent struct {
	Commitment *commitment.Commitment
	Neighbor   identity.ID
}

// endregion ///////////////////////////////////////////////////////////////////////////////////////////////////////////

// region EpochCommitmentRequestReceivedEvent //////////////////////////////////////////////////////////////////////////

type EpochCommitmentRequestReceivedEvent struct {
	CommitmentID commitment.ID
	Source       identity.ID
}

// endregion ///////////////////////////////////////////////////////////////////////////////////////////////////////////

// region ErrorEvent ///////////////////////////////////////////////////////////////////////////////////////////////////

type ErrorEvent struct {
	Error  error
	Source identity.ID
}

// endregion ///////////////////////////////////////////////////////////////////////////////////////////////////////////
